#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	src_count;

	src_count = 0;
	while (src[src_count] != '\0')
		src_count++;
	if (dstsize == 0)
		return (src_count);
	i = 0;
	while (src[i] != '\0' && i < (dstsize -1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_count);
}
